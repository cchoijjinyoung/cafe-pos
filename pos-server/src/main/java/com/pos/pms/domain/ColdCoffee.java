package com.pos.pms.domain;

public class ColdCoffee {
  private int price;
  private String name;
  private String size;
  private int no;

  public int getNo() {
    return no;
  }
  public void setNo(int no) {
    this.no = no;
  }
  public int getPrice() {
    return price;
  }
  public void setPrice(int price) {
    this.price = price;
  }
  public String getName() {
    return name;
  }
  public void setName(String name) {
    this.name = name;
  }
  public String getSize() {
    return size;
  }
  public void setSize(String size) {
    this.size = size;
  }



}
